package VariableAndDataTypes;

public class Variable {
    public static void main(String[] args) {
        int a = 50;
        int b = 5;
        System.out.println(a);
        System.out.println(b);

        String name = "Motilal Gupta";
        System.out.println(name);

        a = 60;
        System.out.println(a);
    }
}
